    private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton25ActionPerformed
        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select *from patient");

            ResultSet set = stmt.executeQuery();

            String check_id;
            check_id = jTextField20.getText();

            String id = "", first_name = "", second_name = "", age = "", address = "", gender = "", pstatus = "", phone = "", city = "", pdate = "", illness = "", ward_n = "";

            while (set.next()) {
                id = set.getString("patient_id");

                if (check_id.equals(id)) {
                    first_name = set.getString("first_name");
                    second_name = set.getString("second_name");
                    age = set.getString("age");
                    address = set.getString("address");
                    gender = set.getString("gender");
                    phone = set.getString("phone");
                    city = set.getString("city");
                    pstatus = set.getString("Pstatus");
                    pdate = set.getString("Pdate");
                    illness = set.getString("illness");
                    ward_n = set.getString("ward_n");
                    break;
                }

            }
            //check if it empty********************************
            if ("".equals(check_id)) {
                JOptionPane.showMessageDialog(this, "please write Patient ID first before click search", "ALARM", JOptionPane.ERROR_MESSAGE);
            } //**********check for exit ***********
            else if (!check_id.equals(id)) {

                JOptionPane.showMessageDialog(this, "The Patient isn't Exist", "Sorry", JOptionPane.INFORMATION_MESSAGE);
                jTextField8.setText("");
                jTextField16.setText("");
                jTextField12.setText("");
                jTextField17.setText("");
                jTextField13.setText("");
                jTextField14.setText("");
                jTextField18.setText("");
                jTextField19.setText("");
                jTextField15.setText("");
                jComboBox13.setSelectedIndex(0);
                jComboBox14.setSelectedIndex(0);
            } else {
                jTextField8.setText(first_name);
                jTextField16.setText(second_name);
                jTextField12.setText(age);
                jTextField17.setText(pdate);
                jTextField13.setText(address);
                jTextField14.setText(city);
                jTextField18.setText(phone);
                jTextField19.setText(illness);
                jTextField15.setText(ward_n);
                //************* check for status ***********
     
                 if ("Single".equals(pstatus)) 
                    jComboBox9.setSelectedIndex(2);
                 else 
                    jComboBox9.setSelectedIndex(1);
                
                //**********************************************************************
                
                //******************* check for gender ***********************
                if ("Male".equals(gender)) 
                    jComboBox10.setSelectedIndex(1);
                    
                else if ("Female".equals(gender)) 
                    jComboBox10.setSelectedIndex(2);
                
                    //********* to disable  enterein data(patient_id)after search done well **************
                    
                jTextField20.enable(false);

            }
        } catch (SQLException ex) {
            System.out.println("Error" + ex.getErrorCode());
        }
    }//GEN-LAST:event_jButton25ActionPerformed

