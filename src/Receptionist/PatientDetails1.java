                stmt.setInt(3, mage);
                stmt.setString(4, address);
                stmt.setString(5, gender);
                stmt.setInt(6, mphone);
                stmt.setString(7, city);
                stmt.setString(8, status);
                stmt.setString(9, date);
                stmt.setString(10, illness);
                stmt.setInt(11, mward_n);
                stmt.setInt(12, mpatient_id);

                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Done... DATA Saved","Confirm",JOptionPane.INFORMATION_MESSAGE);
                jTextField20.setText("");
                jTextField8.setText("");
                jTextField16.setText("");
                jTextField12.setText("");
