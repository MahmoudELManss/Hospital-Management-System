    private void jTextField4FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField4FocusLost
        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select *from doctor");

            ResultSet set = stmt.executeQuery();

            String check_id;
            check_id = jTextField4.getText();

            String id = "", first_name = "", second_name = "";

            while (set.next()) {
                id = set.getString("doctor_id");

                if (check_id.equals(id)) {
                    first_name = set.getString("first_name");
                    second_name = set.getString("second_name");
                    break;
                }

            }
            //check if it empty********************************
            if ("".equals(check_id)) {
                JOptionPane.showMessageDialog(this, "please write doctor ID first before click search", "ALARM", JOptionPane.ERROR_MESSAGE);
            } //************** check for exist*********************************
            else if (!check_id.equals(id)) {
                JOptionPane.showMessageDialog(this, "The Doctor isn't Exist", "Sorry", JOptionPane.INFORMATION_MESSAGE);
                jTextField4.setText("");
                jTextField6.setText("");

            } else {
                jTextField6.setText(first_name + " " + second_name);

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please, review the user guide you made something wrong", "Sorry", 3);
            System.out.println("Error" + ex.getErrorCode());
        }

    }//GEN-LAST:event_jTextField4FocusLost

