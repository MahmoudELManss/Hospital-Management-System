    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select * from give");
            PreparedStatement stmt2 = con.prepareStatement("select * from patient ");
            PreparedStatement stmt3 = con.prepareStatement("select * from doctor ");
            PreparedStatement stmt4 = con.prepareStatement("select * from prescription ");
            String checkPrescription_id = jTextField5.getText();
            String checkPatient_id = jTextField3.getText();
            String checkDoctor_id = jTextField4.getText();
            String cDate = jTextField7.getText();
            String cDescription = jTextArea1.getText();
            String patient_id, doctor_id = "", prescription_id = "";
            ResultSet set = stmt.executeQuery();
            while (set.next()) {
                patient_id = set.getString("patient_id");
                doctor_id = set.getString("doctor_id");
                prescription_id = set.getString("prescription_id");
                break;
            }
            if ("".equals(checkPrescription_id) || "".equals(checkPatient_id) || "".equals(checkDoctor_id) || "".equals(cDate) || "".equals(cDescription)) {
                JOptionPane.showMessageDialog(this, "please Fill all the data first before click \"Search\"", "ALARM", JOptionPane.ERROR_MESSAGE);
            } else if (checkPrescription_id.equals(prescription_id)) {
                JOptionPane.showMessageDialog(this, " this Prescription's ID is Exist, please choose another one", "Sorry", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextArea1.setText("");

            } else {
                try {
                    Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
                    PreparedStatement s = c.prepareStatement("insert into give values(?,?,?)");
                    PreparedStatement s2 = c.prepareStatement("insert into prescription values(?,?,?)");
                    s.setInt(1, Integer.parseInt(checkPatient_id));
                    s.setInt(3, Integer.parseInt(checkDoctor_id));
                    s.setInt(2, Integer.parseInt(checkPrescription_id));
                    s2.setInt(1, Integer.parseInt(checkPrescription_id));
                    s2.setString(2, cDescription);
                    s2.setString(3, cDate);
                    s2.executeUpdate();
                    s.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Added successfully :D", "done!", JOptionPane.PLAIN_MESSAGE);
                    jTextField5.setText("");
                    jTextField1.setText("");
                    jTextField6.setText("");

                    jTextField4.setText("");
                    jTextField3.setText("");

                    jTextField7.setText("");
                    jTextArea1.setText("");

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Please, review the user guide you made something wrong", "Sorry", 3);
                    System.out.println("Error" + ex.getErrorCode());
                }

            }

        } catch (SQLException ex) {
            System.out.println("Error" + ex.getErrorCode());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

