    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select * from doctor");
            ResultSet set = stmt.executeQuery();
            String check_id;
            String id = "";
            check_id = jTextField54.getText();
            while (set.next()) {
                id = set.getString("doctor_id");
                if (check_id.equals(id)) {
                    jTextField46.setText(set.getString("first_name"));
                    jTextField51.setText(set.getString("second_name"));
                    jTextField22.setText(set.getString("age"));
                    jTextField68.setText(set.getString("Ddate"));
                    jTextField48.setText(set.getString("address"));
                    jTextField49.setText(set.getString("city"));
                    jTextField64.setText(set.getString("phone"));
                    jTextField50.setText(set.getString("salary"));
                    jTextField69.setText(set.getString("specialist"));
                    jTextField52.setText(set.getString("Dpassword"));
                    if ("Male".equals(set.getString("gender"))) {
                        jComboBox16.setSelectedIndex(1);
                    } else {
                        jComboBox16.setSelectedIndex(2);
                    }
                    if ("Single".equals(set.getString("Dstatus"))) {
                        jComboBox5.setSelectedIndex(2);
                    } else {
                        jComboBox5.setSelectedIndex(1);
                    }
                    break;
                }
            }
            if (check_id.equals("")) {
                JOptionPane.showMessageDialog(null, "Please, enter an ID to search about its doctor", "Sorry", 2);
            } else if (!check_id.equals(id)) {
                JOptionPane.showMessageDialog(null, "this ID doesn't exist", "wrong", 0);
                jTextField46.setText("");
                jTextField51.setText("");
                jTextField22.setText("");
                jTextField68.setText("");
                jTextField48.setText("");
                jTextField49.setText("");
                jTextField64.setText("");
                jTextField50.setText("");
                jTextField69.setText("");
                jTextField52.setText("");
                jComboBox16.setSelectedIndex(0);
                jComboBox5.setSelectedIndex(0);

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please, review the user guide you made something wrong", "Sorry", 3);
            System.out.println("Error" + ex.getErrorCode());
        }
    }//GEN-LAST:event_jButton12ActionPerformed

