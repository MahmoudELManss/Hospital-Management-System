    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton24ActionPerformed

        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hdbs", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select * from  outdoor_patient");

            ResultSet set = stmt.executeQuery();

            DefaultTableModel dm = new DefaultTableModel();

            dm.addColumn("patient_id");
            dm.addColumn("first_name");
            dm.addColumn("second_name");
            dm.addColumn("age");
            dm.addColumn("address");
            dm.addColumn("gender");
            dm.addColumn("phone");
            dm.addColumn("city");
            dm.addColumn("pstatus");
            dm.addColumn("pdate");
            dm.addColumn("illness");

            while (set.next()) {

                String r[] = {set.getString(1), set.getString(2),
                    set.getString(3), set.getString(4),
                    set.getString(5), set.getString(6), set.getString(7),
                    set.getString(8), set.getString(9), set.getString(10),
                    set.getString(11)};
                dm.addRow(r);
            }
            outdoor_patient.setModel(dm);

        } catch (SQLException ex) {
            System.out.println("Error" + ex.getErrorCode());
        }

    }//GEN-LAST:event_jButton24ActionPerformed

